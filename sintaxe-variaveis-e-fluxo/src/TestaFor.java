
public class TestaFor {
	
	public static void main(String[] args) {
		
		for (int contador = 0; contador <= 10; contador++) {
			System.out.println(contador);
			
		}
		
		/*No while a variavel contador criada dentro sempre reseta na hora do looping, ja no for
		 * a variavel contador nao reseta no looping, por isso for eh mais usado.
		 */
	}

}
